package com.fabien_astiasaran.ori_massages_api.mappers;

import com.fabien_astiasaran.ori_massages_api.dtos.SlotCreate;
import com.fabien_astiasaran.ori_massages_api.dtos.SlotResponse;
import com.fabien_astiasaran.ori_massages_api.entities.Slot;

public class SlotMapper {

    public Slot toEntity(SlotCreate slot){
        Slot entity = new Slot();
        entity.setBeginAt(slot.beginAt());
        entity.setEndAt(slot.endReal());
        entity.setStatus("");
        return entity;
    }

    public SlotResponse toResponse(Slot slot){
        return new SlotResponse(
                slot.getId(),
                slot.getBeginAt(),
                slot.getEndAt(),
                slot.getEndAt(),
                slot.getDate().getDate(),
                WorkingHoursMapper.toResponse(slot.getWorkingHours()),
                PrestationMapper.toResponse(slot.getPrestation())
        );
    }
}
